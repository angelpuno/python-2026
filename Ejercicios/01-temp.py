listacelcius = [12.5, 14.2, 11.8]

promedio = sum(listacelcius)/len(listacelcius)
print(f"el promedio de las temperaturas es:{promedio}")

mayor= max(listacelcius)
print(mayor)

menor= min(listacelcius)
print(menor)

diferencia = mayor-menor
print(f"la diferencia entre las temperaturas maxima y minima {diferencia:.1f}")